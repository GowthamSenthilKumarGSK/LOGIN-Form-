package newpackage;

/**
 *
 * @author SONY
 */
public class User {
    String name;
    String email;
    String code;

    /**
     *
     */
    public User(){
        
    }

    /**
     *
     * @param name
     * @param email
     * @param code
     */
    public User(String name,String email,String code){
        this.name=name;
        this.email=email;
        this.code=code;
    }

    /**
     *
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     *
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     *
     * @return
     */
    public String getEmail() {
        return email;
    }

    /**
     *
     * @param email
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     *
     * @return
     */
    public String getCode() {
        return code;
    }

    /**
     *
     * @param code
     */
    public void setCode(String code) {
        this.code = code;
    }
}
